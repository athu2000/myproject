void main(){
  var size = 'L';

  switch(size){
    case 'S':
      print("The shirt size is S");
      break;
    case 'M':
      print("The shirt size is M");
      break;
    case 'L':
      print("The shirt size is L");
      break;
    case 'XL':
      print("The shirt size is XL");
      break;
    default:
      print("The shirt size is not available");
  }
}